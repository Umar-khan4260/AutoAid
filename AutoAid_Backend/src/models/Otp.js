const mongoose = require('mongoose');

const OtpSchema = new mongoose.Schema({
  email: {
    type: String,
    required: true,
  },
  otp: {
    type: String,
    required: true,
  },
  userData: {
    uid: String,
    fullName: String,
    contactNumber: String,
    role: String
  },
  createdAt: {
    type: Date,
    default: Date.now,
    expires: 300, // Document automatically deleted after 5 minutes (300 seconds)
  },
});

module.exports = mongoose.model('Otp', OtpSchema);
